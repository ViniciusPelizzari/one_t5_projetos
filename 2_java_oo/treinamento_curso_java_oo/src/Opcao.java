public class Opcao {

    private int opcaoCliente;
    private String opcaoImprimir;

    public int getOpcaoCliente() {
        return opcaoCliente;
    }

    public void setOpcaoCliente(int opcao) {
        this.opcaoCliente = opcao;
    }

    public String getOpcaoImprimir() {
        return opcaoImprimir;
    }

    public void setOpcaoImprimir(String opcaoImprimir) {
        this.opcaoImprimir = opcaoImprimir;
    }
}
