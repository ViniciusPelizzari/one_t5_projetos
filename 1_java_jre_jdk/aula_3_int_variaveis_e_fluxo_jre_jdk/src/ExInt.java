public class ExInt {
    public static void main(String[] args) {
        //pode ser declarada seu tipo
        int idade;
        //e depois declarar seu valor
        idade = 19;

        //ou pode ser feito as duas coisas ao mesmo tempo
        int peso = 80;

        System.out.println("Idade: " + idade + " | Peso: " + peso);
    }
}
